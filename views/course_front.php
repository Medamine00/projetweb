if (isset($_POST['add_to_wishlist'])) {
    $course_id = $_POST['course_id'];
    $session_id = session_id(); // Get the current session ID to track users in the wishlist

    try {
        // Check if the course exists in the courses table
        $sql_check_course = "SELECT COUNT(*) FROM courses WHERE id = :course_id";
        $stmt_check_course = $conn->prepare($sql_check_course);
        $stmt_check_course->bindParam(':course_id', $course_id);
        $stmt_check_course->execute();
        $course_exists = $stmt_check_course->fetchColumn();

        if ($course_exists == 0) {
            echo "<div class='alert alert-warning'>This course does not exist.</div>";
        } else {
            // Check if course is already in the wishlist for this session
            $sql_check_wishlist = "SELECT COUNT(*) FROM wishlist WHERE course_id = :course_id AND session_id = :session_id";
            $stmt_check_wishlist = $conn->prepare($sql_check_wishlist);
            $stmt_check_wishlist->bindParam(':course_id', $course_id);
            $stmt_check_wishlist->bindParam(':session_id', $session_id);
            $stmt_check_wishlist->execute();
            $exists = $stmt_check_wishlist->fetchColumn();

            if ($exists == 0) {
                // Insert course into the wishlist
                $sql = "INSERT INTO wishlist (course_id, session_id, date_added) VALUES (:course_id, :session_id, NOW())";
                $stmt = $conn->prepare($sql);
                $stmt->bindParam(':course_id', $course_id);
                $stmt->bindParam(':session_id', $session_id);
                $stmt->execute();
                echo "<div class='alert alert-success'>Course added to wishlist!</div>";
            } else {
                echo "<div class='alert alert-info'>This course is already in your wishlist.</div>";
            }
        }
    } catch (PDOException $e) {
