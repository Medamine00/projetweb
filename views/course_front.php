    // Gérer le clic sur les étoiles pour mettre à jour la notation
    document.querySelectorAll('.star-rating .fa-star').forEach(star => {
        star.addEventListener('click', function() {
            const ratingValue = this.getAttribute('data-value');
            const courseId = this.closest('.star-rating').getAttribute('data-course-id');
            
            // Dé-sélectionner toutes les étoiles
            document.querySelectorAll(`.star-rating[data-course-id="${courseId}"] .fa-star`).forEach(s => s.classList.remove('checked'));

            // Mettre en surbrillance les étoiles sélectionnées
            for (let i = 0; i < ratingValue; i++) {
                document.querySelectorAll(`.star-rating[data-course-id="${courseId}"] .fa-star`)[i].classList.add('checked');
            }

            // Définir la valeur de la notation dans le formulaire caché
            document.querySelector(`#rating_${courseId}`).value = ratingValue;
        });
    });

    // Gérer le formulaire "Add to Wishlist"
    document.querySelectorAll('.add-to-wishlist-form').forEach(form => {
        form.addEventListener('submit', function(event) {
            const alreadyExists = this.getAttribute('data-exists') === 'true';
            if (alreadyExists) {
                event.preventDefault(); // Empêcher la soumission du formulaire
                alert('This course is already in your wishlist!');
            }
